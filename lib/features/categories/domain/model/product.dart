class Product {
  final int? id;
  final String? name;
  final double? weight;
  final String? weightUnit;
  final double? price;
  final String? discountedPrice;
  final String? sku;
  final int? quantity;
  final String? description;
  final String? productType;
  final String? primaryImage;

  Product({
    this.id,
    this.name,
    this.weight,
    this.weightUnit,
    this.price,
    this.discountedPrice,
    this.sku,
    this.quantity,
    this.description,
    this.productType,
    this.primaryImage,
  });
}
